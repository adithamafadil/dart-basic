void main() {
  /// Tipe data String dapat dimasukkan dengan segala
  /// huruf, angka, dan simbol dengan memasukkannya kedalam
  /// petik satu atau petik dua
  String stringSingleQuoteExample = 'My name is Fadil!';
  String stringDoubleQuoteExample = "I'm a Software Engineer";

  /// num dapat menerima semua format angka.
  num numberIntegerExample = 123;
  num numberDecimalExample = 123.123;
  num numberNegativeExample = -123;

  /// int hanya menerima bilangan bulat
  int intExample = 200;
  int intNegativeExample = -200;

  /// double bisa menerima bilangan desimal
  /// dan bilangan bulat
  double doubleExample = 321.0;
  double doubleNegativeExample = -321.0;

  double doubleIntegerExample = 321;
  double doubleIntegerNegativeExample = -321;
}
