void main() {
  /// Comparison
  print(3 > 10); // False
  print(3 < 10); // True

  print(5 <= 5); // True
  print(5 >= 5); // True

  print(10 == 10); // True
  print(10 != 10); // False

  /// And Logic
  print(true && false); // False
  print(true && true); // True
  print(false && false); // False

  /// OR Logic
  print(true || false); // True
  print(true || true); // True
  print(false || false); // False

  /// Negation
  print(!true); // False
  print(!false); // True
}
